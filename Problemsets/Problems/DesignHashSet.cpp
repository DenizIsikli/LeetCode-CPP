#include <unordered_map>

using namespace std;

class MyHashSet {
public:
    unordered_map<int, bool> hashSet;

    MyHashSet() {}
    
    void add(int key) {
        hashSet[key] = true;
    }
    
    void remove(int key) {
        hashSet[key] = false;
    }
    
    bool contains(int key) {
        return hashSet[key];
    }
};

